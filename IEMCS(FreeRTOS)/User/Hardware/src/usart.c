void usart1_sendStr(uint8_t *str)
{
    unsigned int k = 0;

    do
    {
        HAL_UART_Transmit(&huart1, (uint8_t *)(str + k), 1, 1000);
        k++;
    }
    while(*(str + k) != '\0');
}

